interface IEntity {
    id: number;
    name: string;
    variant: number;
}

const Entities: { [key: string]: IEntity } = jsonEntity.reduce((acc, entity) => {

    const key = entity.name.replace(/\s+/g, "");
    acc[key] = entity;
    return acc;
  }, {} as { [key: string]: IEntity });

function findEntityById(id: number, variant?: number): IEntity | undefined {
    const entities = Object.values(Entities);
    return entities.find(entity => 
        entity.id === id && (variant === undefined || entity.variant === variant)
    );
}

const entitiesByIdAndVariant: { [id: number]: { [variant: number]: number } } = jsonEntity.reduce((acc, entity, index) => {
    if (!acc[entity.id]) {
      acc[entity.id] = {};
    }
    acc[entity.id][entity.variant] = index; // L'index dans le tableau JSON
    return acc;
  }, {} as { [id: number]: { [variant: number]: number } });

export { jsonEntity, Entities, findEntityById, entitiesByIdAndVariant, IEntity };